package daphne.example.cathos_proyecto.HomesApp;

//datos para el HomesApp
public class DataStruct {

    private String id;
    private String description;
    private String directions;
    private String disclainer;
    private Double lat;
    private Integer listPrice;
    private Double log;
    private String primary_photo;




    public String getDescription() {
        return description;
    }

    public void setDescription(String descripcion) {
        this.description = descripcion;
    }

    public String getDirections() {
        return directions;
    }

    public void setDirections(String directions) {
        this.directions = directions;
    }

    public String getDisclainer() {
        return disclainer;
    }

    public void setDisclainer(String disclainer) {
        this.disclainer = disclainer;
    }

    public Double getLat() {
        return lat;
    }

    public void setLat(Double lat) {
        this.lat = lat;
    }

    public Integer getListPrice() {
        return listPrice;
    }

    public void setListPrice(Integer listPrice) {
        this.listPrice = listPrice;
    }

    public  Double getLog() {
        return log;
    }

    public void setLog(Double log) {
        this.log = log;
    }

    public String getPrimary_photo() {
        return primary_photo;
    }

    public void setPrimary_photo(String primary_photo) {
        this.primary_photo = primary_photo;
    }



    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }


}
